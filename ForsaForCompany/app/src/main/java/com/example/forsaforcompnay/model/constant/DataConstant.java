package com.example.forsaforcompnay.model.constant;

import java.util.ArrayList;

public class DataConstant {
    private ArrayList<Skills> skills;
    private ArrayList<Degrees> degrees;
    private ArrayList<Universities> universities;
    private ArrayList<Specialize> specialize;

    public ArrayList<Skills> getSkills() {
        return skills;
    }

    public void setSkills(ArrayList<Skills> skills) {
        this.skills = skills;
    }

    public ArrayList<Degrees> getDegrees() {
        return degrees;
    }

    public void setDegrees(ArrayList<Degrees> degrees) {
        this.degrees = degrees;
    }

    public ArrayList<Universities> getUniversities() {
        return universities;
    }

    public void setUniversities(ArrayList<Universities> universities) {
        this.universities = universities;
    }

    public ArrayList<Specialize> getSpecialize() {
        return specialize;
    }

    public void setSpecialize(ArrayList<Specialize> specialize) {
        this.specialize = specialize;
    }
}
